public class Adicao {
    // Declaração das variáveis de instância
    private double mais1;
    private double mais2;

    // Criação do construtor da classe Adicao
    public Adicao(double mais1, double mais2) {
        this.mais1 = mais1;
        this.mais2 = mais2;
    }

    // Criação do método calcular, que realiza a soma
    public double calcularad() {
        return mais1 + mais2;
    }
}
